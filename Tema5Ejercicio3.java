public class Tema5Ejercicio3 {

	/**
	 * Ejercicio3
	 * Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle do-while.
	 */
	public static void main(String[] args) {
		int i = 5;
		do {
			System.out.println(i);
			i += 5;
		} while (i < 51);

	}

}
