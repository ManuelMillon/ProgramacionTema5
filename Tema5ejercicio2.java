public class Tema5ejercicio2 {

	/**
	 * @param argsEjercicio 2 Muestra los números múltiplos de 5 de 0 a 100
	 *  utilizando un bucle while
	 */
	public static void main(String[] args) {

		int i = 5;

		while (i < 101) {
			System.out.println(i);
			i += 5;
		}

	}

}
